// ============================================================
// 简易检索函数（基于关键词匹配 + 分类加权）
// ============================================================
export function searchKnowledgeBase(query: string, topN: number = 5): KBArticle[] {
  const qLower = query.toLowerCase();
  const scored = KNOWLEDGE_BASE.map(article => {
    let score = 0;
    // 关键词匹配
    for (const kw of article.keywords) {
      if (qLower.includes(kw.toLowerCase())) score += 3;
      // 部分匹配
      if (kw.length > 2 && qLower.includes(kw.slice(0, Math.floor(kw.length / 2)).toLowerCase())) score += 1;
    }
    // 标题匹配
    if (qLower.split(/\s+/).some(w => article.title.toLowerCase().includes(w))) score += 5;
    // 内容包含查询词
    const contentLower = article.content.toLowerCase();
    const queryWords = qLower.split(/\s+/).filter(w => w.length > 1);
    for (const w of queryWords) {
      if (contentLower.includes(w)) score += 1;
    }
    return { article, score };
  });
  scored.sort((a, b) => b.score - a.score);
  return scored.filter(s => s.score > 2).slice(0, topN).map(s => s.article);
}
