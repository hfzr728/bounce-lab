// ---- User Prompt 构建器 ----
export function buildDiagnosisUserPrompt(
  answersSummary: string,
  _dimensionScores: string,
  strengths: string,
  weaknesses: string,
  injuryRisk: string
): string {
  return "以下是一位运动员的详细弹跳能力评估数据：\n\n【运动员逐项数据】\n" + answersSummary + "\n\n【受伤风险评估】" + injuryRisk + "\n\n【参考信息】优势方向：" + strengths + "；薄弱方向：" + weaknesses + "\n\n请逐条阅读以上每一项数据，用具体数字进行对比分析。结合运动员的运动专项特点（如果有），分析该专项常见的伤病风险和对发力率/起跳方式的要求。不要使用任何编号或内部术语。全程用中文。";
}

export function buildPlanUserPrompt(
  planInput: string,
  weaknesses: string,
  injuryRisk: string,
  diagnosisSummary: string,
  injury?: InjuryInfo,
): string {
  // 生成伤病约束文本
  const injuryConstraints = injury ? buildInjuryConstraintText(injury) : "";

  return "以下是一位运动员的训练条件与诊断信息：\n\n【训练条件】" + planInput + "\n【薄弱方向】" + weaknesses + "\n【受伤风险】" + injuryRisk + "\n【诊断摘要】" + diagnosisSummary +
    "\n\n请基于以上数据，生成完整的个性化训练计划。" +
    injuryConstraints +
    "\n\n关键要求（违反将导致计划不可用）：\n- 必须覆盖第1周到第12周，缺一周都不合格。token有限，所以每句话都要精简。\n- 每日方案只需写：动作名、组数*次数、负荷、休息。不要写原理说明！\n- 第9-12周也必须完整，每周所有训练日都要列完再进下一周。\n- 全部使用中文。";
}
